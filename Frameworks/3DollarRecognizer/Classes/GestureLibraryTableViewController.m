// Customize the number of rows in the table view.
- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [[gestureDB.gestureDict allKeys] count];
}


// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Set up the cell...
	NSString * name = [[gestureDB.gestureDict allKeys] objectAtIndex:indexPath.row];
	NSMutableArray * arrayForName = [gestureDB.gestureDict objectForKey:name];
	cell.textLabel.text = name;
	cell.detailTextLabel.text = [NSString stringWithFormat:@"%d Gestures",[arrayForName count]];
	
	UIImage *aIconImage = [UIImage imageWithContentsOfFile:[[NSBundle mainBundle] pathForResource:[NSString stringWithFormat:@"%@64",name] ofType:@"png"]];
	
	if (aIconImage == nil) {
		aIconImage = [UIImage imageWithContentsOfFile:[[NSBundle mainBundle] pathForResource:@"unknown64"ofType:@"png"]];
	}
	
	cell.imageView.image = aIconImage;
	
    return cell;
}


